/*
 * ✅ Purpose:
Prints a message to the console and moves the cursor to the next line.

🔍 How it works:
System is a class from java.lang.
out is a static field of type PrintStream.
println() is a method of PrintStream that prints the argument and appends a newline (\n).
 */

package com.hcl.statements.printing;
public class PrintlnExample {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("World");
    }
}
