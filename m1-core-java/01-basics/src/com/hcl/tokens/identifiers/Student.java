package com.hcl.tokens.identifiers;
public class Student {
    private String name;       // 'name' is an identifier
    private int age;           // 'age' is an identifier

    public void setName(String name) { // 'setName' and 'name' are identifiers
        this.name = name;
    }

    public int getAge() {      // 'getAge' is an identifier
        return age;
    }
}
