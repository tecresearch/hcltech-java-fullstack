
/*
 * 4. String Literal
Question: Write a program that concatenates two string literals and prints the result.
 */
package com.hcl.tokens.leterals;
public class StringLiteralExample {
    public static void main(String[] args) {
        String greeting = "Hello, ";
        String name = "Java!";
        String message = greeting + name;

        System.out.println(message);
    }
}
